class Dataset_ETT_hour_causal(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h', seasonal_patterns=None):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

        self.seg = 20
        self.num_pseg = int(np.ceil(self.__len__() / float(self.seg)))


    def __read_data__(self):
        #self.scaler = StandardScaler()
        self.scaler = OnlineHeterogeneousScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))

        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], 1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0) 

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        """
            目标:根据给定的索引（index）动态切片，返回一个 Batch 中的单条样本
            seq_len: 输入序列长度（如96）
            label_len：起始标记长度（如48）
            pred_len：预测序列长度（如192）
            data_x：经过归一化后的历史数据（形状 [总样本数, 特征数]）
            data_y：与 data_x 相同（这里用于构造未来窗口）
            data_stamp：时间特征编码（形状 [总样本数, 时间特征维度]）
            seg：设为20，用于计算 self.num_pseg
            s_begin = index
            s_end = s_begin + self.seq_len：输入序列结束位置
            r_begin = s_end - self.label_len：预测起始位置（即解码器开始位置）
            r_end = r_begin + self.label_len + self.pred_len：预测结束位置（包含 label_len 和 pred_len）
        """
        # 1. 窗口边界索引的计算 (Index Calculation):首先通过传入的 index 计算出时间窗口在全局数据中的截取位置。
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        #2. 因果特征矩阵的构建 (seq_xall)
        #这是 _causal 数据集最核心、最特殊的一段逻辑。普通的预测只取一个未来的预测窗口，而这里构建了一个全景的未来窗口矩阵。
        """
        计算过程：循环执行了 seq_len + 1 次。对于输入序列区间 $[s\_begin, s\_end]$ 中的每一个时间步 $j$，
                代码都会从该时间步起，向后截取一段长度为 pred_len 的未来数据块，并追加到列表中。
        物理含义：它构建了一个滑动未来窗口集合。这主要是为了在 exp_causal_forecasting.py 中，让模型通过一个可学习
                的掩码（learnable_mask）来寻找过去某个时间点与未来预测窗口之间的因果/平移关系。
        张量形状：最终得到的 seq_xall 是一个三维数组，形状为 $(seq\_len + 1, pred\_len, feature\_dim)$。
        """
        seq_xall = []
        for j in range(self.seq_len +1):
            seq_xall.append(self.data_y[s_begin+j:s_begin+self.pred_len+j])
        seq_xall = np.array(seq_xall)

        # 3. 标准序列与协变量特征的提取
        # 这部分提取了常规 Transformer 架构所需的基础张量。
        """
        seq_x_mark和seq_y_mark就是用来生成时间特征编码（Temporal Embedding）的，
        它们强行给那些冰冷的数值（如用电量、油温）打上了“时间烙印”，让模型知道这个数值具体发生在哪一刻。
        seq_x_mark 的原理：总结过去 (Contextualizing History)。
                     用途：它与 seq_x（历史数值）一一对应，输入给 Encoder。
        seq_y_mark 的原理：指引未来 (Guiding Future Generation)。
                     用途：它与解码器的输入 dec_inp 对应，输入给 Decoder。
        seq_x: 编码器的输入历史数值特征（如油温、负载等），长度为 seq_len。
        seq_y: 解码器的目标数值特征，总长度为 label_len + pred_len。模型训练时将预测其后半段。
        seq_x_mark / seq_y_mark: 对应时间步的时间协变量特征（Time-covariates）。
            如果 timeenc=0，这些是月份、星期、小时等分类值的离散编码；
            如果 timeenc=1，则是经过傅里叶变换或固定函数映射后的连续时间特征（Time Features）。
            它们用于为模型提供时间维度的位置感知。
        """
        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        # 4. 域偏移标识符 (domain)
        # 针对非平稳时间序列（Non-stationary）或分布偏移（Distribution Shift）研究加入的特殊标识。
        """
        计算过程：用当前样本的起始位置 index 除以总的片段数 num_pseg（在类的 __init__ 中，
                num_pseg 是由总长度除以分段大小 seg = 20 向上取整得到的）。
        物理含义：生成一个 $[0, 1]$ 范围内的连续浮点数，代表当前这条样本在整个时间轴上处于什么阶段
                （比如 0.1 代表在数据集最早期，0.9 代表在数据集晚期）。这个变量通常在分布外（OOD，Out-of-Distribution）
                泛化或非平稳模型中，用来衡量概念漂移（Concept Drift）的程度。
        """
        domain = index / self.num_pseg

        """
            seq_xall: 用于因果发现的滑动未来目标矩阵。
            seq_x: 历史输入数据。
            seq_y: 包含 Start Token 的目标真实数据。
            seq_x_mark: 历史输入的时间戳特征。
            seq_y_mark: 目标数据的时间戳特征。
            domain: 样本在时间轴上的相对位置标识（域标识）。
        """
        return seq_xall, seq_x, seq_y, seq_x_mark, seq_y_mark, domain

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


