def get_delay(x, y, k):
    tau = 0
    sim = 0
    for i in range(k+1):
        temp_sim = np.dot(x[i:len(x)-k+i], y[k:])/(np.linalg.norm(x[i:len(x)-k+i])*np.linalg.norm(y[k:]))
        if abs(temp_sim) >= sim:
            sim = abs(temp_sim)
            tau = i
    return tau  


